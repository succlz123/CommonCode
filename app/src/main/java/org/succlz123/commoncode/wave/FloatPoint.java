public class FloatPoint {
    public float x;
    public float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public FloatPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }
}